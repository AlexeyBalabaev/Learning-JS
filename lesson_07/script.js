/* compare */
alert( 2 > 1 );     // true
alert( 2 == 1 );    // false
alert( 2 != 1 );    // true

alert( 'Z' > 'A' );       // true
alert( 'Dog' > 'Doc' );   // true
alert( 'Cats' > 'Cat' );  // true

alert( '2' > 1 );    // true
alert( '01' == 1 );  // true

alert( true == 1 );    // true
alert( false == 0 );   // true

alert( 0 === false );  // false

alert( null === undefined );  // false
alert( null == undefined );   // true